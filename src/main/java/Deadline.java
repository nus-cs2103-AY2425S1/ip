public class Deadline extends Task {
    protected String by;
    private static final long serialVersionUID = 1L;

    public Deadline(String description, String by) {
        super(description);
        this.by = by;
    }

    @Override
    public String toString() {
        return "[D]" + super.toString() + " (by: " + this.by + ")";
    }
}
