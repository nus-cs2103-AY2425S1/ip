package Hamyo.Tasks;

/**
 * Represents the possible types of Tasks in the Hamyo chatbot.
 *
 * @author Han Yu
 */
public enum TaskType {
    /**
     * Represents To-Do Task.
     */
    TODO,
    /**
     * Represents Deadline Task.
     */
    DEADLINE,
    /**
     * Represents Event Task.
     */
    EVENT
}
