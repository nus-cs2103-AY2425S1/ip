package weeny.task;

/**
 * Enum representing different types of tasks.
 */
public enum TaskType {
    /**
     * Represents a to-do task.
     */
    TODO,

    /**
     * Represents a deadline task with a due date.
     */
    DEADLINE,

    /**
     * Represents an event with a start and end time.
     */
    EVENT
}
