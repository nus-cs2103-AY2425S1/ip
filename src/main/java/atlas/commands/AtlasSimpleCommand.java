package atlas.commands;

/**
 * Contains all possible short atlas commands.
 */
public enum AtlasSimpleCommand {
    B, // Short for BYE
    L, // Short for LIST
    M, // Short for MARK
    U, // Short for UNMARK
    R, // Short for DELETE
    F, // Short for FIND
    T, // Short for TODO
    D, // Short for DEADLINE
    E, // Short for EVENT
    H // Short for HELP
}
