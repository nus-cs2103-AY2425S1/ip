import java.util.Scanner;

public class Sentinel {

    public static int index = 0;
    public static String[] items = new String[100];

    public static void main(String[] args) {
        String logo = 
        " _____                                                                                      _____ \n" + 
        "( ___ )                                                                                    ( ___ )\n" + 
        " |   |~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~|   | \n" + 
        " |   |                                                                                      |   | \n" + 
        " |   |   ________  _______   ________   _________  ___  ________   _______   ___            |   | \n" + 
        " |   |  |\\   ____\\|\\  ___ \\ |\\   ___  \\|\\___   ___\\\\  \\|\\   ___  \\|\\  ___ \\ |\\  \\           |   | \n" + 
        " |   |  \\ \\  \\___|\\ \\   __/|\\ \\  \\\\ \\  \\|___ \\  \\_\\ \\  \\ \\  \\\\ \\  \\ \\   __/|\\ \\  \\          |   | \n" + 
        " |   |   \\ \\_____  \\ \\  \\_|/_\\ \\  \\\\ \\  \\   \\ \\  \\ \\ \\  \\ \\  \\\\ \\  \\ \\  \\_|/_\\ \\  \\         |   | \n" + 
        " |   |    \\|____|\\  \\ \\  \\_|\\ \\ \\  \\\\ \\  \\   \\ \\  \\ \\ \\  \\ \\  \\\\ \\  \\ \\  \\_|\\ \\ \\  \\____    |   | \n" + 
        " |   |      ____\\_\\  \\ \\_______\\ \\__\\  \\__\\   \\ \\__\\ \\ \\__\\ \\__\\\\ \\__\\ \\_______\\ \\_______\\  |   | \n" + 
        " |   |     |\\_________\\|_______|\\|__| \\|__|    \\|__|  \\|__|\\|__| \\|__|\\|_______\\|_______|   |   | \n" + 
        " |   |     \\|_________|                                                                     |   | \n" + 
        " |   |                                                                                      |   | \n" + 
        " |___|~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~|___| \n" + 
        "(_____)                                                                                    (_____)";

        System.out.println("Hello from\n" + logo);
        System.out.println("\nWhat can I do for you?\n____________________________________________________________\n");
        Scanner sc = new Scanner(System.in);
        String input = sc.nextLine();
        System.out.println("____________________________________________________________\n");
        while (true) {
            if (input.equals("bye"))
                break;
            else if (input.equals("list")) {
                for (int i = 0; i < index; i++) {
                    System.out.println("\t" + (i + 1) + ". " + items[i]);
                }
            } else {
                items[index++] = input;
                System.out.println("added: " + input);
            }
            System.out.println("____________________________________________________________\n");
            input = sc.nextLine();
            System.out.println("____________________________________________________________\n");
        }
        System.out.println("Bye. Hope to see you again soon!");
        sc.close();
    }
}
