package yapyap;

/**
 * Represents the set of commands that can be executed by the YapperBot.
 * Each enum value corresponds to a specific user command that YapperBot can recognize and process.
 */
public enum Commands {
    BYE,
    LIST,
    MARK,
    UNMARK,
    TODO,
    DEADLINE,
    EVENT,
    DELETE,
    FIND,
    HELP,
    OTHERS;
}

