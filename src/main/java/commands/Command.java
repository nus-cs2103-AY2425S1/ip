package commands;

/**
 * Represents the different commands that can be issued by the user
 * in the application. Each command corresponds to a specific action
 * or task that the application can perform.
 */
public enum Command {
    HI,
    BYE,
    LIST,
    MARK,
    UNMARK,
    DELETE,
    FIND,
    TAG,
    HELP,
    TODO,
    DEADLINE,
    EVENT
}
